#include "libft.h"

char	*ft_strndup(char *s, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)ft_calloc(n + 1, sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (i < n)
	{
		str[i] = s[i];
		i++;
	}
	return (str);
}
